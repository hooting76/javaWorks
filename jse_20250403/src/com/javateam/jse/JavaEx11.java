package com.javateam.jse;

public class JavaEx11 {

	public static void main(String[] args) {

		// 연산(operation)
		// =, +, * : 연산자(operator)
		// sum, 1, 2, 3 : 피연산자(operand), 항 
		// int sum = 1 + 2 * 3;
		int sum = (1 + 2) * 3;
		System.out.printf("합계 = %d%n", sum);
		
		int n = 1;
		n += 3; // n = n + 3;
		System.out.println(n);
	}

}
